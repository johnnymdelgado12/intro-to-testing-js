function helloWorld() {
    return "Hello, World!";
}

function sayHello(input) {
    var name;

    switch (input) {
        case (input === true) || input === undefined :
            name = "Hello, World!";
        break;
        case input:
            name =  "Hello, " + input + "!";
        break;
    }
}


